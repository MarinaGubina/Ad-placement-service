CREATE TABLE image
(
    id        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fileSize  INTEGER NOT NULL,
    mediaType TEXT    NOT NULL,
    data      BYTEA    NOT NULL
);

